class User3 {
    private String item;
    private String month;
    private int year;
    private int cost;
    
    public User3 (String item, String month, int year, int cost)
    {
    this.item=item;
    this.month=month;
    this.year=year;
    this.cost=cost;
    
    }
    
    public String getitem()
    {
    return item;
    }
    public String getmonth()
    {
    return month;
    }      
    public int getyear()
    {
        return year;
    }
    public int getcost()
    {
        return cost;
    }

}
